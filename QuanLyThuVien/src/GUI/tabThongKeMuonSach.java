    private void btnCountableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCountableActionPerformed
        String selected = cbDate.getSelectedItem().toString();
        java.util.Date startDate = dateStart.getDate();
        java.util.Date endDate = dateEnd.getDate();

        if (startDate == null || endDate == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn đầy đủ thông tin!");
            return;
        }
        model = new DefaultTableModel();
        model.addColumn("Thời gian");
        model.addColumn("Số lượng phiếu mượn");
        java.sql.Date startD = new Date(startDate.getTime());
        java.sql.Date startE = new Date(endDate.getTime());
        DefaultTableModel newmodel = new DefaultTableModel();
        newmodel = phieuMuon.thongKeMuonSachTable(selected, startD, startE, model);
        if(newmodel == null){
            JOptionPane.showMessageDialog(this, "Lỗi");
            return;
        }
        tableBaoCaoSoLuong.setModel(newmodel);
    }//GEN-LAST:event_btnCountableActionPerformed

    private void btnChartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChartActionPerformed
        String selected = cbDate.getSelectedItem().toString();
        java.util.Date startDate = dateStart.getDate();
        java.util.Date endDate = dateEnd.getDate();

        if (startDate == null || endDate == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn đầy đủ thông tin!");
            return;
        }
        if (startDate.after(endDate)) {
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu phải trước hoặc bằng ngày kết thúc!");
            return;
        }
        java.sql.Date startD = new Date(startDate.getTime());
        java.sql.Date startE = new Date(endDate.getTime());
        JFreeChart barChart = phieuMuon.thongKeMuonSachChart(selected, startD, startE);
        ChartFrame chartFrame = new ChartFrame("Biểu đồ thống kê", barChart);
        chartFrame.setSize(800, 600);
        chartFrame.setLocationRelativeTo(null);
        chartFrame.setVisible(true);
    }//GEN-LAST:event_btnChartActionPerformed

