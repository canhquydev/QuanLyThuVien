    public void layDuLieu(){
        model = new DefaultTableModel();
        model.addColumn("Mã phiếu mượn");
        model.addColumn("Mã sách");
        model.addColumn("Mã người mượn");
        model.addColumn("Số lượng");
        model.addColumn("Ngày mượn");
        
        for(PhieuMuon pm: dsPhieuMuon){
            model.addRow(new Object[]{
                pm.getMaPhieuMuon().trim(),
                pm.getMaSach().trim(),
                pm.getMaNguoiMuon().trim(),
                pm.getSoLuong(),
                pm.getNgayMuon(),
                pm.getNgayTra(),
                pm.getTinhTrang(),
                pm.getSoLanGiaHan()
            });
        }
        tableBaoCaoSoLuong.setModel(model);
        cbDate.removeAllItems();
        for(String s: list){
            cbDate.addItem(s);
        }
    }

